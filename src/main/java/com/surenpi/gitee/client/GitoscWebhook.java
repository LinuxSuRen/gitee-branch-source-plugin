package com.surenpi.gitee.client;

public class GitoscWebhook {
    private String create_at;
    private String id;
    private String issues_events;
    private String merge_requests_events;
    private String note_events;
    private String password;
    private String project_id;
    private String push_events;
    private String result;
    private String result_code;
    private String tag_push_events;
    private String url;

    public String getCreate_at() {
        return create_at;
    }

    public void setCreate_at(String create_at) {
        this.create_at = create_at;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIssues_events() {
        return issues_events;
    }

    public void setIssues_events(String issues_events) {
        this.issues_events = issues_events;
    }

    public String getMerge_requests_events() {
        return merge_requests_events;
    }

    public void setMerge_requests_events(String merge_requests_events) {
        this.merge_requests_events = merge_requests_events;
    }

    public String getNote_events() {
        return note_events;
    }

    public void setNote_events(String note_events) {
        this.note_events = note_events;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getProject_id() {
        return project_id;
    }

    public void setProject_id(String project_id) {
        this.project_id = project_id;
    }

    public String getPush_events() {
        return push_events;
    }

    public void setPush_events(String push_events) {
        this.push_events = push_events;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getResult_code() {
        return result_code;
    }

    public void setResult_code(String result_code) {
        this.result_code = result_code;
    }

    public String getTag_push_events() {
        return tag_push_events;
    }

    public void setTag_push_events(String tag_push_events) {
        this.tag_push_events = tag_push_events;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
